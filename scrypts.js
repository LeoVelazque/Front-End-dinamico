//nav responsivo
function responsiveMenu() {
    var x = document.getElementById("nav");
    if (x.className === "") {
        x.className = "responsive";
    }
    else (x.className = "")
}

//login
function registrarse() {
    document.getElementById("login").style.display = "block";
}

function salir() {
    document.getElementById("login").style.display = "none";
}

